    private void btnZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZapamtiActionPerformed
        // TODO add your handling code here:
        if(staza == null){
            JOptionPane.showMessageDialog(this, "Morate prvo kreirati stazu");
            return;
        }
        napuniDomenskiObjekat();
        try {
            Kontroler.getInstanca().zapamtiStazu(staza);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio stazu");
            isprazniGrafickiObjekat();
            isprazniDomenskiObjekat();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti stazu");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnZapamtiActionPerformed

