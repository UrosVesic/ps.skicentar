    private void prepare() {
        List<OpstiDomenskiObjekat> skiCentri = new ArrayList<>();
        cmbSkiCentar.removeAllItems();
        try {
            skiCentri = Kontroler.getInstanca().ucitajListuSkiCentara(skiCentri);
            for (OpstiDomenskiObjekat skiCentar : skiCentri) {
                cmbSkiCentar.addItem((SkiCentar) skiCentar);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da vrati listu ski centara");
        }
    }

    private void napuniGrafickiObjekat() throws SQLException {
        txtBrojStaze.setText(String.valueOf(staza.getBrojStaze()));
        txtNazivStaze.setText("");

    }

    private void napuniDomenskiObjekat() {
        staza.setBrojStaze(Long.parseLong(txtBrojStaze.getText()));
        staza.setNazivStaze(txtNazivStaze.getText());
        staza.setSkiCentar((SkiCentar) cmbSkiCentar.getSelectedItem());
        staza.setTipStaze((String) cmbTipStaze.getSelectedItem());
        
    }

    private void isprazniGrafickiObjekat() {
        txtBrojStaze.setText("");
        txtNazivStaze.setText("");
    }

    private void isprazniDomenskiObjekat() {
        staza = null;
    }
